export const generateExportPayload = async (businessId: string, jobId: string, format: string): Promise<ExportPayload | null> => {
  const job = await prisma.reportsCenterExportJob.findFirst({ where: { id: jobId, businessId } });
  if (!job) return null;

  const rows = buildExportRows();
  const safeType = job.type.replace(/\s+/g, '-').toLowerCase();

  if (format === 'excel') {
    const body = rows.map(row => row.join('\\t')).join('\\n');
    return { body, filename: `${safeType}.xls`, contentType: 'application/vnd.ms-excel' };
  }

  const body = rows.map(row => row.map(cell => `"${cell}"`).join(',')).join('\n');
  return { body, filename: `${safeType}.csv`, contentType: 'text/csv' };
};
