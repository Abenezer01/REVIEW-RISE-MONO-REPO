import crypto from 'crypto';

type Frequency = 'daily' | 'weekly' | 'monthly';
type ExportPayload = { body: string | Buffer; filename: string; contentType: string };

const DEFAULT_CONFIG = {
  preset: 'executive',
  sections: {
    seo: true,
    reputation: true,
    listings: true
  },
  sectionOrder: ['seo', 'reputation', 'listings'],
  whiteLabel: {
    logoDataUrl: null,
    primaryColor: '#0F172A',
    accentColor: '#2563EB',
    title: 'Monthly Performance Report',
    footer: 'Confidential | Generated by ReviewRise',
    intro: 'A focused summary of performance highlights and next steps.'
  }
};

const hashPassword = (value: string) =>
  crypto.createHash('sha256').update(value).digest('hex');

const addFrequency = (date: Date, frequency: Frequency) => {
  const next = new Date(date);
  if (frequency === 'daily') next.setDate(next.getDate() + 1);
  if (frequency === 'weekly') next.setDate(next.getDate() + 7);
  if (frequency === 'monthly') next.setMonth(next.getMonth() + 1);
  return next;
};

export const getConfig = async (businessId: string) => {
  const existing = await prisma.reportsCenterConfig.findUnique({
    where: { businessId }
  });

  if (existing) return existing;

  return prisma.reportsCenterConfig.create({
    data: {
      businessId,
      preset: DEFAULT_CONFIG.preset,
      sections: DEFAULT_CONFIG.sections,
      sectionOrder: DEFAULT_CONFIG.sectionOrder,
      whiteLabel: DEFAULT_CONFIG.whiteLabel
    }
  });
};

export const updateConfig = async (businessId: string, payload: any) => {
  const config = await getConfig(businessId);
  return prisma.reportsCenterConfig.update({
    where: { id: config.id },
    data: {
      preset: payload.preset ?? config.preset,
      sections: payload.sections ?? config.sections,
      sectionOrder: payload.sectionOrder ?? config.sectionOrder,
      whiteLabel: payload.whiteLabel ?? config.whiteLabel
    }
  });
};

export const getSchedule = async (businessId: string) => {
  const config = await getConfig(businessId);
  const schedule = await prisma.reportsCenterSchedule.findUnique({
    where: { configId: config.id }
  });

  if (schedule) return schedule;

  return prisma.reportsCenterSchedule.create({
    data: {
      businessId,
      configId: config.id,
      frequency: 'weekly',
      recipients: ['client@agency.com'],
      nextRunAt: addFrequency(new Date(), 'weekly')
    }
  });
};

export const updateSchedule = async (businessId: string, payload: any) => {
  const config = await getConfig(businessId);
  const existing = await prisma.reportsCenterSchedule.findUnique({
    where: { configId: config.id }
  });

  if (existing) {
    return prisma.reportsCenterSchedule.update({
      where: { id: existing.id },
      data: {
        frequency: payload.frequency ?? existing.frequency,
        recipients: payload.recipients ?? existing.recipients,
        triggerEnabled: payload.triggerEnabled ?? existing.triggerEnabled,
        seoDropThreshold: payload.seoDropThreshold ?? existing.seoDropThreshold,
        nextRunAt: payload.nextRunAt ?? existing.nextRunAt
      }
    });
  }

  return prisma.reportsCenterSchedule.create({
    data: {
      businessId,
      configId: config.id,
      frequency: payload.frequency ?? 'weekly',
      recipients: payload.recipients ?? ['client@agency.com'],
      triggerEnabled: payload.triggerEnabled ?? false,
      seoDropThreshold: payload.seoDropThreshold ?? 5,
      nextRunAt: payload.nextRunAt ?? addFrequency(new Date(), payload.frequency ?? 'weekly')
    }
  });
};

export const generateReportRun = async (businessId: string, options?: { password?: string | null }) => {
  const config = await getConfig(businessId);
  const whiteLabel = config.whiteLabel as any;
  const shareToken = crypto.randomBytes(8).toString('hex');
  const passwordHash = options?.password ? hashPassword(options.password) : null;
  const name = whiteLabel?.title || 'Business Report';
  const now = new Date();

  const run = await prisma.reportsCenterRun.create({
    data: {
      businessId,
      configId: config.id,
      name,
      type: 'PDF',
      status: 'Sent',
      generatedAt: now,
      deliveredAt: now,
      shareToken,
      sharePasswordHash: passwordHash,
      webUrl: `/reports-center/share/${shareToken}`,
      meta: {
        sections: config.sections,
        sectionOrder: config.sectionOrder,
        whiteLabel
      }
    }
  });

  return run;
};

