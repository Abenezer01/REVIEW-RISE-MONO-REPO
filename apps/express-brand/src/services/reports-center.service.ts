export const generateReportPdf = async (runId: string, businessId: string): Promise<Buffer> => {
  const run = await prisma.reportsCenterRun.findFirst({ where: { id: runId, businessId } });
  if (!run) {
    throw new Error('Report not found');
  }

  const config = await prisma.reportsCenterConfig.findFirst({ where: { businessId } });
  const sectionOrder = (config?.sectionOrder || ['seo', 'reputation', 'listings']) as string[];
  const sections = normalizeSections((run as any)?.sections || (config as any)?.sections, sectionOrder);
  const whiteLabel = (config?.whiteLabel || {}) as any;
  const sectionContent = await buildSectionContent(businessId);
  return buildReportsCenterPdf({
    sections,
    whiteLabel: whiteLabel as any,
    formatSectionTitle,
    sectionContent
  });
};

