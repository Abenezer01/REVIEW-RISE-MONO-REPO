const formatSectionTitle = (key: string) => {
  if (key === 'seo') return 'SEO Performance';
  if (key === 'reputation') return 'Reputation & Reviews';
  if (key === 'listings') return 'Listings Consistency';
  return key;
};

const formatDateShort = (value?: Date | null) => {
  if (!value) return 'N/A';
  return value.toLocaleDateString();
};

const buildSectionContent = async (businessId: string) => {
  const latestVisibility = await prisma.visibilitySnapshot.findFirst({
    where: { businessId },
    orderBy: { capturedAt: 'desc' }
  });

  const reviewStats = await ReviewService.getReviewStats(businessId).catch(() => ({
    totalReviews: 0,
    averageRating: 0
  }));

  const breakdown = (latestVisibility?.breakdown || {}) as Record<string, any>;
  const listingsScore =
    breakdown.consistencyScore ?? breakdown.listingsScore ?? breakdown.citationsScore ?? null;

  return {
    seo: {
      bullets: [
        `Visibility score: ${latestVisibility?.score ?? 0}/100`,
        `Latest snapshot: ${formatDateShort(latestVisibility?.capturedAt)}`
      ]
    },
    reputation: {
      bullets: [
        `Average rating: ${reviewStats.averageRating || 0} / 5`,
        `Total reviews: ${reviewStats.totalReviews || 0}`
      ]
    },
    listings: {
      bullets: listingsScore !== null
        ? [
            `Listings consistency score: ${Number(listingsScore).toFixed(0)} / 100`,
            `Latest snapshot: ${formatDateShort(latestVisibility?.capturedAt)}`
          ]
        : [
            'Listings consistency data is not available yet.',
            'Connect directory sources to start tracking accuracy.'
          ]
    }
  };
};

const normalizeSections = (
  sectionsInput: unknown,
  sectionOrder: string[]
) => {
  if (Array.isArray(sectionsInput)) {
    return sectionsInput.length ? sectionsInput : sectionOrder;
  }

  if (sectionsInput && typeof sectionsInput === 'object') {
    const flags = sectionsInput as Record<string, boolean>;
    return sectionOrder.filter((section) => flags[section] !== false);
  }

  return sectionOrder;
};

